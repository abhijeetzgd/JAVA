class LeftShiftDemo{
  public static void main(String [] args){
      int num1 = 88;
      int num2 = -20;
      int result = 0;

      result = num1 << 4;
      System.out.println(result);

      result = num2 << 3;
      System.out.println(result);

  }
}
//Answer will be same as positive value with negative sign.
