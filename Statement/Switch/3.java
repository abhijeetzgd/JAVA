class SwitchDemo{

  public static void main( String [] args){

    short x=100;

    switch(x){

      case 100:
        System.out.println("First case");
        break;


      case 200:
        System.out.println("Second case");
        break;


      case 300:
        System.out.println("Third case");
        break;
    }
  }
}
