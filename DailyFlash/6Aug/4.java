class Demo{

  public static void main(String [] args){

    System.out.println("Reverse order of number from 1 -100 ");

    for(int i =100;i>=1;i--){
      System.out.println(i);

}
    System.out.println("Number Divisible by 4 are ");
    for(int i =100;i>=1;i--){

      if(i%4 == 0){
        System.out.println(i);
        
      }
    }
  }
}
